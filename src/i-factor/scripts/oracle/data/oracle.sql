CREATE TABLESPACE ifactor
  NOLOGGING 
  DATAFILE '&1\ifactor1.dbf' SIZE 100M REUSE 
  AUTOEXTEND 
  ON NEXT 100M MAXSIZE UNLIMITED EXTENT MANAGEMENT LOCAL 
  SEGMENT SPACE MANAGEMENT  AUTO 
/

CREATE USER ifactor PROFILE DEFAULT
  IDENTIFIED BY ifactor DEFAULT TABLESPACE ifactor
  ACCOUNT UNLOCK;
GRANT CONNECT TO ifactor;
GRANT RESOURCE TO ifactor;
GRANT UNLIMITED TABLESPACE TO ifactor;
/